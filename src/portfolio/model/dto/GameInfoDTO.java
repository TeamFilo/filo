package portfolio.model.dto;

public class GameInfoDTO {
	
	private Integer gameCate;
	private String name;
	private Integer needPoint;
	
	public Integer getGameCate() {
		return gameCate;
	}
	public void setGameCate(Integer gameCate) {
		this.gameCate = gameCate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getNeedPoint() {
		return needPoint;
	}
	public void setNeedPoint(Integer needPoint) {
		this.needPoint = needPoint;
	}
	
	

}
